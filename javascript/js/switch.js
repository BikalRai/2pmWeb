const day = 1;

switch (day) {
	case 1:
		console.log("Sunday");
		break;
	case 2:
		console.log("Monday");
		break;
	case 3:
		console.log("Tuesday");
		break;
	case 4:
		console.log("Wednesday");
		break;
	case 5:
		console.log("Thursday");
		break;
	case 6:
		console.log("Friday");
		break;
	case 7:
		console.log("Saturday");
		break;
	default:
		console.log("Invalid day!!!");
}

const color = 'yellow';

switch(color) {
	case 'blue':
		console.log("changing color to blue...");
		break;
	case 'green':
		console.log("changing color to green...");
		break;
	case 'red':
		console.log("changing color to red...");
		break;
	default:
		console.log("Color not registered!!");

}